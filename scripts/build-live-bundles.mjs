mkdirSync('src/styles', { recursive: true });
writeFileSync('src/styles/d2aa-live.css', bundle(cssFiles, 'CSS'));
writeFileSync('src/live/d2aa-live.js', bundle(jsFiles, 'JS'));
writeFileSync('src/live/manifest.json', `${JSON.stringify({ css: cssFiles.map(sourcePath), js: jsFiles.map(sourcePath) }, null, 2)}\n`);
