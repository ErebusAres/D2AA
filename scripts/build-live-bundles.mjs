function banner(type, files) {
  return [
    `/* D2AA live ${type} bundle.`,
    ' * Generated by scripts/build-live-bundles.mjs from the original D2AA live assets.',
    ' * Keep D2AA.html pointed at this bundle; edit source files above and rebuild when needed.',
    ` * Sources: ${files.map(sourcePath).join(', ')}`,
    ' */',
    ''
  ].join('\n');
}

function bundle(files, type) {
  return banner(type, files) + files.map((file) => {
    const body = readFileSync(sourcePath(file), 'utf8').trimEnd();
    return `\n/* ===== ${basename(file)} ===== */\n${body}\n`;
  }).join('\n');
}

